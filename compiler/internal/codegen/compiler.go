// defineCharToStringFunc emits an LLVM IR function that converts a Unicode
// codepoint (i32) to its UTF-8 encoded string representation.
// Uses cascading range checks with bitwise encoding.
// Replaces the C runtime promise_char_to_string.
func (c *Compiler) defineCharToStringFunc() {
	cpParam := ir.NewParam("cp", irtypes.I32)
	fn := c.module.NewFunc("promise_char_to_string", irtypes.I8Ptr, cpParam)

	bufType := irtypes.NewArray(4, irtypes.I8)
	zero32 := constant.NewInt(irtypes.I32, 0)

	entry := fn.NewBlock(".entry")
	buf := entry.NewAlloca(bufType)

	// Check: cp < 0x80?
	oneByte := fn.NewBlock("one_byte")
	check2 := fn.NewBlock("check_2")
	lt80 := entry.NewICmp(enum.IPredULT, cpParam, constant.NewInt(irtypes.I32, 0x80))
	entry.NewCondBr(lt80, oneByte, check2)

	// one_byte: buf[0] = (i8)cp; return string_new(buf, 1)
	b0_1 := oneByte.NewTrunc(cpParam, irtypes.I8)
	slot0_1 := oneByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	oneByte.NewStore(b0_1, slot0_1)
	bufPtr1 := oneByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	r1 := oneByte.NewCall(c.funcs["promise_string_new"], bufPtr1, constant.NewInt(irtypes.I64, 1))
	oneByte.NewRet(r1)

	// check_2: cp < 0x800?
	twoByte := fn.NewBlock("two_byte")
	check3 := fn.NewBlock("check_3")
	lt800 := check2.NewICmp(enum.IPredULT, cpParam, constant.NewInt(irtypes.I32, 0x800))
	check2.NewCondBr(lt800, twoByte, check3)

	// two_byte: buf[0] = 0xC0 | (cp >> 6); buf[1] = 0x80 | (cp & 0x3F)
	sh6_2 := twoByte.NewLShr(cpParam, constant.NewInt(irtypes.I32, 6))
	b0_2 := twoByte.NewOr(sh6_2, constant.NewInt(irtypes.I32, 0xC0))
	b0_2_8 := twoByte.NewTrunc(b0_2, irtypes.I8)
	slot0_2 := twoByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	twoByte.NewStore(b0_2_8, slot0_2)

	m1_2 := twoByte.NewAnd(cpParam, constant.NewInt(irtypes.I32, 0x3F))
	b1_2 := twoByte.NewOr(m1_2, constant.NewInt(irtypes.I32, 0x80))
	b1_2_8 := twoByte.NewTrunc(b1_2, irtypes.I8)
	slot1_2 := twoByte.NewGetElementPtr(bufType, buf, zero32, constant.NewInt(irtypes.I32, 1))
	twoByte.NewStore(b1_2_8, slot1_2)

	bufPtr2 := twoByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	r2 := twoByte.NewCall(c.funcs["promise_string_new"], bufPtr2, constant.NewInt(irtypes.I64, 2))
	twoByte.NewRet(r2)

	// check_3: cp < 0x10000?
	threeByte := fn.NewBlock("three_byte")
	fourByte := fn.NewBlock("four_byte")
	lt10000 := check3.NewICmp(enum.IPredULT, cpParam, constant.NewInt(irtypes.I32, 0x10000))
	check3.NewCondBr(lt10000, threeByte, fourByte)

	// three_byte: 3-byte UTF-8 encoding
	sh12_3 := threeByte.NewLShr(cpParam, constant.NewInt(irtypes.I32, 12))
	b0_3 := threeByte.NewOr(sh12_3, constant.NewInt(irtypes.I32, 0xE0))
	b0_3_8 := threeByte.NewTrunc(b0_3, irtypes.I8)
	slot0_3 := threeByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	threeByte.NewStore(b0_3_8, slot0_3)

	sh6_3 := threeByte.NewLShr(cpParam, constant.NewInt(irtypes.I32, 6))
	m1_3 := threeByte.NewAnd(sh6_3, constant.NewInt(irtypes.I32, 0x3F))
	b1_3 := threeByte.NewOr(m1_3, constant.NewInt(irtypes.I32, 0x80))
	b1_3_8 := threeByte.NewTrunc(b1_3, irtypes.I8)
	slot1_3 := threeByte.NewGetElementPtr(bufType, buf, zero32, constant.NewInt(irtypes.I32, 1))
	threeByte.NewStore(b1_3_8, slot1_3)

	m2_3 := threeByte.NewAnd(cpParam, constant.NewInt(irtypes.I32, 0x3F))
	b2_3 := threeByte.NewOr(m2_3, constant.NewInt(irtypes.I32, 0x80))
	b2_3_8 := threeByte.NewTrunc(b2_3, irtypes.I8)
	slot2_3 := threeByte.NewGetElementPtr(bufType, buf, zero32, constant.NewInt(irtypes.I32, 2))
	threeByte.NewStore(b2_3_8, slot2_3)

	bufPtr3 := threeByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	r3 := threeByte.NewCall(c.funcs["promise_string_new"], bufPtr3, constant.NewInt(irtypes.I64, 3))
	threeByte.NewRet(r3)

	// four_byte: 4-byte UTF-8 encoding
	sh18_4 := fourByte.NewLShr(cpParam, constant.NewInt(irtypes.I32, 18))
	b0_4 := fourByte.NewOr(sh18_4, constant.NewInt(irtypes.I32, 0xF0))
	b0_4_8 := fourByte.NewTrunc(b0_4, irtypes.I8)
	slot0_4 := fourByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	fourByte.NewStore(b0_4_8, slot0_4)

	sh12_4 := fourByte.NewLShr(cpParam, constant.NewInt(irtypes.I32, 12))
	m1_4 := fourByte.NewAnd(sh12_4, constant.NewInt(irtypes.I32, 0x3F))
	b1_4 := fourByte.NewOr(m1_4, constant.NewInt(irtypes.I32, 0x80))
	b1_4_8 := fourByte.NewTrunc(b1_4, irtypes.I8)
	slot1_4 := fourByte.NewGetElementPtr(bufType, buf, zero32, constant.NewInt(irtypes.I32, 1))
	fourByte.NewStore(b1_4_8, slot1_4)

	sh6_4 := fourByte.NewLShr(cpParam, constant.NewInt(irtypes.I32, 6))
	m2_4 := fourByte.NewAnd(sh6_4, constant.NewInt(irtypes.I32, 0x3F))
	b2_4 := fourByte.NewOr(m2_4, constant.NewInt(irtypes.I32, 0x80))
	b2_4_8 := fourByte.NewTrunc(b2_4, irtypes.I8)
	slot2_4 := fourByte.NewGetElementPtr(bufType, buf, zero32, constant.NewInt(irtypes.I32, 2))
	fourByte.NewStore(b2_4_8, slot2_4)

	m3_4 := fourByte.NewAnd(cpParam, constant.NewInt(irtypes.I32, 0x3F))
	b3_4 := fourByte.NewOr(m3_4, constant.NewInt(irtypes.I32, 0x80))
	b3_4_8 := fourByte.NewTrunc(b3_4, irtypes.I8)
	slot3_4 := fourByte.NewGetElementPtr(bufType, buf, zero32, constant.NewInt(irtypes.I32, 3))
	fourByte.NewStore(b3_4_8, slot3_4)

	bufPtr4 := fourByte.NewGetElementPtr(bufType, buf, zero32, zero32)
	r4 := fourByte.NewCall(c.funcs["promise_string_new"], bufPtr4, constant.NewInt(irtypes.I64, 4))
	fourByte.NewRet(r4)

	c.funcs["promise_char_to_string"] = fn
}

// declareFuncs creates LLVM function declarations for all FuncDecl nodes with bodies (pass 1).
// Generic functions (with TypeParams) are skipped — handled by declareMonoFuncs.
// Std functions get mangled LLVM names (__std_X) to avoid collisions with user functions.
func (c *Compiler) declareFuncs(file *ast.File) {
	for _, decl := range file.Decls {
		fd, ok := decl.(*ast.FuncDecl)
		if !ok {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}
		isEmbed := c.info.Embeds[fd] != nil
		if fd.Body == nil && !isEmbed {
			continue // extern — already handled by declareExterns
		}
		if len(fd.TypeParams) > 0 {
			continue // generic — handled by monomorphization
		}

		// Module-level setters are stored under "name$set" in sema scope.
		scopeName := fd.Name
		if fd.IsSetter {
			scopeName = fd.Name + "$set"
		}

		obj := c.lookupFunc(scopeName)
		if obj == nil {
			continue
		}
		sig, ok := obj.Type().(*types.Signature)
		if !ok {
			continue
		}

		retType := irtypes.Type(irtypes.Void)
		if c.info.GeneratorFuncs[fd] != nil {
			// Generator functions return {i8*, i8*} (handle + yield slot)
			retType = generatorValueType()
		} else if sig.Result() != nil {
			retType = c.resolveType(sig.Result())
		}
		if sig.CanError() && c.info.GeneratorFuncs[fd] == nil {
			retType = computeResultType(retType)
		}

		var params []*ir.Param
		for _, p := range sig.Params() {
			params = append(params, ir.NewParam(p.Name(), c.resolveParamType(p)))
		}

		// C ABI requires main to return i32 and receive argc/argv
		if fd.Name == "main" {
			retType = irtypes.I32
			params = []*ir.Param{
				ir.NewParam("argc", irtypes.I32),
				ir.NewParam("argv", irtypes.NewPointer(irtypes.I8Ptr)),
			}
		}

		fn := c.module.NewFunc(scopeName, retType, params...)
		c.funcs[scopeName] = fn
	}
}

// defineFuncs generates function bodies for all FuncDecl nodes with bodies (pass 2).
// Generic functions (with TypeParams) are skipped — handled by defineMonoFuncs.
func (c *Compiler) defineFuncs(file *ast.File) {
	for _, decl := range file.Decls {
		fd, ok := decl.(*ast.FuncDecl)
		if !ok {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}
		// Handle embed getters — body is nil but we generate it from embedded data
		if embedInfo := c.info.Embeds[fd]; embedInfo != nil {
			fn := c.funcs[fd.Name]
			if fn != nil {
				c.defineEmbedGetter(fd, fn, embedInfo)
			}
			continue
		}
		if fd.Body == nil {
			continue // native function — no body to generate
		}
		if len(fd.TypeParams) > 0 {
			continue // generic — handled by monomorphization
		}
		// Skip main — its body is compiled inline inside .goroutine.main
		// by wrapMainWithScheduler (with inCoroutine=true for proper channel ops).
		if fd.Name == "main" {
			c.mainDecl = fd
			continue
		}
		// Module-level setters are stored under "name$set" in sema scope.
		scopeName := fd.Name
		if fd.IsSetter {
			scopeName = fd.Name + "$set"
		}
		fn := c.funcs[scopeName]
		if fn == nil {
			continue
		}
		// Generator functions get special compilation
		if elemType := c.info.GeneratorFuncs[fd]; elemType != nil {
			c.defineGeneratorFunc(fd, fn, elemType)
			continue
		}
		c.defineFunc(fd, fn)
	}
}

// defineFunc generates the body of a single function.
func (c *Compiler) defineFunc(fd *ast.FuncDecl, fn *ir.Func) {
	c.fn = fn
	c.locals = make(map[string]*ir.InstAlloca)
	c.localNameCount = make(map[string]int)
	c.dropFlags = make(map[string]*ir.InstAlloca)
	c.dropBindings = make(map[string]scopeBinding)
	c.mutRefPtrs = nil
	c.mutRefTypes = nil
	c.blockCounter = 0

	entry := fn.NewBlock(".entry")
	c.block = entry
	c.entryBlock = entry

	// Allocate parameters and store incoming values
	obj := c.lookupFunc(fd.Name)
	if obj == nil {
		return
	}
	sig, ok := obj.Type().(*types.Signature)
	if !ok {
		return
	}
	c.canError = sig.CanError()
	c.currentRetType = sig.Result()

	for i, p := range sig.Params() {
		if p.Name() == "" || p.Name() == "_" {
			continue
		}
		if _, isMutRef := p.Type().(*types.MutRef); isMutRef {
			// MutRef param: caller passes a pointer to its alloca.
			// The param value IS a pointer to the caller's storage —
			// reads load through it, writes store through it.
			innerType := c.resolveType(p.Type())
			if c.mutRefPtrs == nil {
				c.mutRefPtrs = make(map[string]value.Value)
				c.mutRefTypes = make(map[string]irtypes.Type)
			}
			c.mutRefPtrs[p.Name()] = fn.Params[i]
			c.mutRefTypes[p.Name()] = innerType
		} else {
			alloca := entry.NewAlloca(c.resolveType(p.Type()))
			alloca.SetName(c.uniqueLocalName(p.Name() + ".addr"))
			entry.NewStore(fn.Params[i], alloca)
			c.locals[p.Name()] = alloca
		}
	}

	// Coverage: instrument function entry (skip test functions and main)
	if c.shouldInstrument() && !c.isTestFunc(fd.Name) && fd.Name != "main" {
		pos := fd.Pos()
		end := fd.End()
		idx := c.addCoverageRegion(pos.File, pos.Line, end.Line, fd.Name, "function")
		c.emitCoverageIncrement(idx)
	}

	c.genBlock(fd.Body)

	// Ensure the function ends with a terminator
	if c.block != nil && c.block.Term == nil {
		if c.canError {
			resultType := c.currentResultType()
			if isVoidResult(resultType) {
				c.block.NewRet(c.wrapOk(nil, resultType))
			} else {
				c.block.NewRet(c.wrapOk(c.zeroValue(resultType.Fields[1]), resultType))
			}
		} else if _, ok := fn.Sig.RetType.(*irtypes.VoidType); ok {
			c.block.NewRet(nil)
		} else {
			c.block.NewRet(c.zeroValue(fn.Sig.RetType))
		}
	}
}

// lookupFunc finds a function object in sema info by name.
func (c *Compiler) lookupFunc(name string) *types.Func {
	// Walk all recorded scopes
	for _, scope := range c.info.ScopeOrder {
		if obj := scope.Lookup(name); obj != nil {
			if fn, ok := obj.(*types.Func); ok {
				return fn
			}
		}
	}
	return nil
}

// zeroValue returns the zero/default value for an LLVM type.
func (c *Compiler) zeroValue(typ irtypes.Type) constant.Constant {
	switch t := typ.(type) {
	case *irtypes.IntType:
		return constant.NewInt(t, 0)
	case *irtypes.FloatType:
		return constant.NewFloat(t, 0.0)
	case *irtypes.PointerType:
		return constant.NewNull(t)
	case *irtypes.StructType:
		return constant.NewZeroInitializer(t)
	default:
		return constant.NewInt(irtypes.I64, 0)
	}
}

// currentResultType returns the result struct type of the current failable function.
func (c *Compiler) currentResultType() *irtypes.StructType {
	return c.fn.Sig.RetType.(*irtypes.StructType)
}

// wrapOk builds an Ok result struct: { false, val, null } or { false, null } for void.
func (c *Compiler) wrapOk(val value.Value, resultType *irtypes.StructType) value.Value {
	var agg value.Value = constant.NewUndef(resultType)
	agg = c.block.NewInsertValue(agg, constant.NewInt(irtypes.I1, 0), 0)
	if isVoidResult(resultType) {
		agg = c.block.NewInsertValue(agg, constant.NewNull(irtypes.I8Ptr), 1)
	} else {
		agg = c.block.NewInsertValue(agg, val, 1)
		agg = c.block.NewInsertValue(agg, constant.NewNull(irtypes.I8Ptr), 2)
	}
	return agg
}

// wrapError builds an Error result struct: { true, zero, errVal } or { true, errVal } for void.
func (c *Compiler) wrapError(errVal value.Value, resultType *irtypes.StructType) value.Value {
	var agg value.Value = constant.NewUndef(resultType)
	agg = c.block.NewInsertValue(agg, constant.NewInt(irtypes.I1, 1), 0)
	if isVoidResult(resultType) {
		agg = c.block.NewInsertValue(agg, errVal, 1)
	} else {
		agg = c.block.NewInsertValue(agg, c.zeroValue(resultType.Fields[1]), 1)
		agg = c.block.NewInsertValue(agg, errVal, 2)
	}
	return agg
}

// newBlock creates a new basic block in the current function.
func (c *Compiler) newBlock(name string) *ir.Block {
	c.blockCounter++
	return c.fn.NewBlock(fmt.Sprintf("%s.%d", name, c.blockCounter))
}

// createEntryAlloca creates an alloca in the function's entry block.
// This ensures the alloca dominates all uses, which is required by LLVM's
// verifier across all versions. The entry block dominates every block in
// the function, so allocas placed here are always valid.
func (c *Compiler) createEntryAlloca(elemType irtypes.Type) *ir.InstAlloca {
	return c.entryBlock.NewAlloca(elemType)
}

// mangleModuleFuncName produces a module-qualified LLVM function name.
func mangleModuleFuncName(moduleName, funcName string) string {
	return "__mod_" + moduleName + "_" + funcName
}

// mangleModuleMethodName produces a module-qualified LLVM method name.
func mangleModuleMethodName(moduleName, typeName, methodName string, isSetter bool) string {
	base := mangleMethodName(typeName, methodName, isSetter)
	return "__mod_" + moduleName + "_" + base
}

// compileModules inlines all imported module declarations into the current IR module.
// For each module, it temporarily swaps c.info and c.file to the module's context,
// runs the same layout/declare/define pipeline, then restores.
// Modules are compiled in topological order (dependencies before dependents)
// so that a module's types and functions are available when its dependents are compiled.
func (c *Compiler) compileModules() {
	if c.info.ModuleInfos == nil {
		return
	}

	// Build path → IR prefix mapping for alias resolution in genModuleCall.
	for _, modInfo := range c.info.ModuleInfos {
		if modInfo.Path != "" {
			c.moduleCanonical[modInfo.Path] = modInfo.EffectiveIRPrefix()
		}
	}

	// Collect mono instances, func instances, and method instances from the main
	// (user) sema info. These include instances of module-defined types/methods
	// (e.g. Map[string,int], Iterator[int].map[int]) and calls to module generic
	// functions (e.g. sort[int]) that were created by user code. We pass them to
	// compileModule so they are processed under the correct module context.
	mainMonoInstances := collectMonoInstances(c.info, c.spiralInstances)
	mainMonoFuncInstances := collectMonoFuncInstances(c.info, mainMonoInstances)
	mainMonoMethodInstances := collectMonoMethodInstances(c.info, mainMonoInstances)

	// Second pass: resolve type instances from generic function/method bodies (B0134).
	extraFromFuncs := resolveTypeInstancesFromFuncInstances(c.info, mainMonoInstances, mainMonoFuncInstances, mainMonoMethodInstances, c.spiralInstances)
	if len(extraFromFuncs) > 0 {
		c.computeMonoLayouts(extraFromFuncs)
		mainMonoInstances = append(mainMonoInstances, extraFromFuncs...)
	}

	// Also include instances from other modules — a catalog module (e.g., json)
	// might create instances of std types (e.g., Map[string, JsonValue]) that the
	// user code doesn't directly reference. Without this, the std module wouldn't
	// know to monomorphize Map[string, JsonValue] and its methods/getters.
	for _, modInfo := range c.info.ModuleInfos {
		mainMonoInstances = append(mainMonoInstances, modInfo.SemaInfo.Instances...)
		mainMonoFuncInstances = append(mainMonoFuncInstances, modInfo.SemaInfo.FuncInstances...)
		mainMonoMethodInstances = append(mainMonoMethodInstances, modInfo.SemaInfo.MethodInstances...)
	}

	// Use topological order if available, otherwise fall back to map iteration.
	if len(c.info.ModuleOrder) > 0 {
		for _, key := range c.info.ModuleOrder {
			if modInfo, ok := c.info.ModuleInfos[key]; ok {
				c.compileModule(modInfo, mainMonoInstances, mainMonoFuncInstances, mainMonoMethodInstances)
			}
		}
	} else {
		for _, modInfo := range c.info.ModuleInfos {
			c.compileModule(modInfo, mainMonoInstances, mainMonoFuncInstances, mainMonoMethodInstances)
		}
	}
}

// compileModule compiles a single module's declarations into the current IR module.
// extraInstances contains mono instances from the caller's sema info (e.g., the
// main file) that may include instances of this module's types; they are merged
// in so that user-created instantiations of module-defined generics are processed
// under the correct module context (module's c.info and c.file).
func (c *Compiler) compileModule(modInfo *sema.ModuleInfo, extraInstances []*types.Instance, extraFuncInstances []*sema.FuncInstance, extraMethodInstances []*sema.MethodInstance) {
	// Save main context
	savedInfo := c.info
	savedFile := c.file
	savedModule := c.compilingModule

	// Switch to module context.
	// Use IRPrefix (derived from GlobalIdentity) for IR symbols — this is stable
	// and globally unique, enabling cross-project .o reuse.
	irName := modInfo.EffectiveIRPrefix()
	c.info = modInfo.SemaInfo
	c.file = modInfo.File
	c.compilingModule = irName
	// Reset per-module string counter so module string constant names are stable
	// (independent of how many string constants main code has emitted so far).
	c.moduleStrCounter = 0

	modFile := modInfo.File

	// 1. Compute enum layouts for module types
	c.computeEnumLayouts(modFile)

	// 2. Compute user type layouts for module types
	c.computeUserTypeLayouts(modFile)

	// 3. Collect mono instances: module's own + any extra from caller that belong
	//    to types defined in this module's file. Transitive expansion uses the
	//    module's sema info so method-body references (e.g. _FnIter[T]) resolve.
	monoInstances := collectMonoInstancesWithExtra(modInfo, modFile, extraInstances, c.spiralInstances)
	c.computeMonoLayouts(monoInstances)
	monoFuncInstances := collectMonoFuncInstancesWithExtra(modInfo.SemaInfo, modFile, extraFuncInstances, monoInstances)
	monoMethodInstances := collectMonoMethodInstancesWithExtra(modInfo.SemaInfo, modFile, extraMethodInstances, monoInstances)
	crossResolveFuncMethodInstances(modInfo.SemaInfo, &monoFuncInstances, &monoMethodInstances)

	// 4. Declare module externs
	modExterns := collectExterns(modFile, modInfo.SemaInfo)
	c.declareModuleExterns(irName, modExterns)

	// 5. Declare method stubs for module types and enums
	c.declareModuleTypeMethods(modFile, irName)
	c.declareModuleEnumMethods(modFile, irName)
	c.declareMonoMethods(modFile, monoInstances)
	c.declareMonoEnumMethods(modFile, monoInstances)
	c.declareMonoSynthesizedDefaults(modFile, monoInstances)
	c.declareSynthesizedModuleDrops(modFile, irName)      // B0158
	c.declareSynthesizedMonoDrops(modFile, monoInstances) // B0158

	// 6. Compute vtable info and emit for module types
	c.computeVtableInfo(modFile)
	c.computeMonoVtableInfo(monoInstances)
	c.emitVtableGlobals(modFile)
	c.emitMonoVtableGlobals(monoInstances)
	c.emitTypeInfoGlobals(modFile)
	c.emitMonoTypeInfoGlobals(monoInstances)

	// 7. Declare and define module functions
	c.declareModuleFuncs(modFile, irName)
	c.declareMonoFuncs(modFile, monoFuncInstances)
	c.declareMonoMethodInstances(modFile, monoMethodInstances)

	// 8. Define module method bodies
	c.defineModuleTypeMethods(modFile, irName)
	c.defineModuleEnumMethods(modFile, irName)
	c.defineMonoMethods(modFile, monoInstances)
	c.defineMonoEnumMethods(modFile, monoInstances)
	c.defineMonoSynthesizedDefaults(modFile, monoInstances)
	c.defineSynthesizedModuleDrops(modFile, irName)      // B0158
	c.defineSynthesizedMonoDrops(modFile, monoInstances) // B0158

	// 9. Define module function bodies
	c.defineModuleFuncs(modFile, irName)
	c.defineMonoFuncs(modFile, monoFuncInstances)
	c.defineMonoMethodInstances(modFile, monoMethodInstances)

	// Restore main context
	c.info = savedInfo
	c.file = savedFile
	c.compilingModule = savedModule
}

// declareModuleExterns declares extern functions from a module, deduplicating
// against already-declared C functions. Registers in moduleExterns for qualified access.
func (c *Compiler) declareModuleExterns(moduleName string, externs []*ExternFunc) {
	// Use the standard extern declaration pipeline (handles dedup, sret, etc.)
	c.declareExterns(externs, c.layouts)

	// Register module externs for qualified access
	for _, ext := range externs {
		key := moduleName + "." + ext.PromiseName
		c.moduleExterns[key] = ext
		// Also register in main externs if no collision
		if _, exists := c.externs[ext.PromiseName]; !exists {
			c.externs[ext.PromiseName] = ext
		}
	}
}

// declareModuleFuncs declares module functions with module-prefixed IR names.
func (c *Compiler) declareModuleFuncs(file *ast.File, moduleName string) {
	for _, decl := range file.Decls {
		fd, ok := decl.(*ast.FuncDecl)
		if !ok || len(fd.TypeParams) > 0 {
			continue
		}
		isEmbed := c.info.Embeds[fd] != nil
		if fd.Body == nil && !isEmbed {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}

		// Module-level setters are stored under "name$set" in sema scope.
		scopeName := fd.Name
		if fd.IsSetter {
			scopeName = fd.Name + "$set"
		}

		obj := c.lookupFunc(scopeName)
		if obj == nil {
			continue
		}
		sig, ok := obj.Type().(*types.Signature)
		if !ok {
			continue
		}

		retType := irtypes.Type(irtypes.Void)
		if c.info.GeneratorFuncs[fd] != nil {
			retType = generatorValueType()
		} else if sig.Result() != nil {
			retType = c.resolveType(sig.Result())
		}
		if sig.CanError() && c.info.GeneratorFuncs[fd] == nil {
			retType = computeResultType(retType)
		}

		var params []*ir.Param
		for _, p := range sig.Params() {
			params = append(params, ir.NewParam(p.Name(), c.resolveParamType(p)))
		}

		irName := mangleModuleFuncName(moduleName, scopeName)
		fn := c.module.NewFunc(irName, retType, params...)

		// Track ownership for separate compilation
		c.moduleOwnedFuncs[irName] = moduleName

		// Register in moduleFuncs for qualified access (mod.property / mod.property$set)
		key := moduleName + "." + scopeName
		c.moduleFuncs[key] = fn

		// Also register in main funcs if no collision (for glob imports)
		if _, shadowed := c.funcs[scopeName]; !shadowed {
			c.funcs[scopeName] = fn
		}
	}
}

// defineModuleFuncs generates bodies for module functions.
func (c *Compiler) defineModuleFuncs(file *ast.File, moduleName string) {
	for _, decl := range file.Decls {
		fd, ok := decl.(*ast.FuncDecl)
		if !ok || len(fd.TypeParams) > 0 {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}

		// Handle embed getters — body is nil but we generate it from embedded data (B0145)
		if embedInfo := c.info.Embeds[fd]; embedInfo != nil {
			scopeName := fd.Name
			key := moduleName + "." + scopeName
			fn, ok := c.moduleFuncs[key]
			if ok {
				c.defineEmbedGetter(fd, fn, embedInfo)
			}
			continue
		}

		if fd.Body == nil {
			continue // native function — no body to generate
		}

		// Module-level setters are stored under "name$set" in sema scope.
		scopeName := fd.Name
		if fd.IsSetter {
			scopeName = fd.Name + "$set"
		}

		key := moduleName + "." + scopeName
		fn, ok := c.moduleFuncs[key]
		if !ok {
			continue
		}

		obj := c.lookupFunc(scopeName)
		if obj == nil {
			continue
		}
		sig, ok := obj.Type().(*types.Signature)
		if !ok {
			continue
		}

		c.fn = fn
		c.block = fn.NewBlock(".entry")
		c.entryBlock = c.block
		c.locals = make(map[string]*ir.InstAlloca)
		c.localNameCount = make(map[string]int)
		c.dropFlags = make(map[string]*ir.InstAlloca)
		c.dropBindings = make(map[string]scopeBinding)
		c.mutRefPtrs = nil
		c.mutRefTypes = nil
		c.scopeBindings = nil
		c.canError = sig.CanError()
		c.currentRetType = sig.Result()
		c.blockCounter = 0

		// Bind parameters to local allocas
		for i, p := range fn.Params {
			sp := sig.Params()[i]
			if _, isMutRef := sp.Type().(*types.MutRef); isMutRef {
				// MutRef param: the LLVM param is a pointer to the caller's storage (B0149)
				innerType := c.resolveType(sp.Type())
				if c.mutRefPtrs == nil {
					c.mutRefPtrs = make(map[string]value.Value)
					c.mutRefTypes = make(map[string]irtypes.Type)
				}
				c.mutRefPtrs[sp.Name()] = p
				c.mutRefTypes[sp.Name()] = innerType
			} else {
				alloca := c.entryBlock.NewAlloca(p.Typ)
				c.entryBlock.NewStore(p, alloca)
				c.locals[sp.Name()] = alloca
			}
		}

		c.genBlock(fd.Body)

		// Add implicit return if block not terminated
		if c.block != nil && c.block.Term == nil {
			if sig.CanError() {
				c.block.NewRet(c.zeroValue(fn.Sig.RetType))
			} else if fn.Sig.RetType == irtypes.Void {
				c.block.NewRet(nil)
			} else {
				c.block.NewRet(c.zeroValue(fn.Sig.RetType))
			}
		}
	}
}

// declareModuleTypeMethods declares method stubs for module types with module-prefixed names.
func (c *Compiler) declareModuleTypeMethods(file *ast.File, moduleName string) {
	for _, decl := range file.Decls {
		td, ok := decl.(*ast.TypeDecl)
		if !ok {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}
		named := c.lookupNamedType(td.Name)
		if named == nil || len(named.TypeParams()) > 0 {
			continue
		}

		for _, md := range td.Methods {
			if md.Body == nil {
				continue
			}
			if len(md.TypeParams) > 0 {
				continue // generic method — handled by mono method instances
			}
			m := c.lookupAnyMethod(named, md.Name, md.IsGetter, md.IsSetter)
			if m == nil || m.Sig() == nil {
				continue
			}

			mangledName := mangleModuleMethodName(moduleName, td.Name, md.Name, md.IsSetter)

			var params []*ir.Param
			if m.Sig().Recv() != nil {
				receiverType := irtypes.Type(irtypes.I8Ptr)
				if isPrimitiveScalar(named) {
					receiverType = llvmNamedType(named)
				}
				params = append(params, ir.NewParam("this", receiverType))
			}
			for _, p := range m.Sig().Params() {
				params = append(params, ir.NewParam(p.Name(), c.resolveParamType(p)))
			}

			retType := irtypes.Type(irtypes.Void)
			if c.info.GeneratorFuncs[md] != nil {
				retType = generatorValueType()
			} else if m.Sig().Result() != nil {
				retType = c.resolveType(m.Sig().Result())
			}
			if m.Sig().CanError() && c.info.GeneratorFuncs[md] == nil {
				retType = computeResultType(retType)
			}

			fn := c.module.NewFunc(mangledName, retType, params...)
			c.funcs[mangledName] = fn

			// Track ownership for separate compilation
			c.moduleOwnedFuncs[mangledName] = moduleName

			// Also register the non-prefixed method name for dispatch within the module
			plainName := mangleMethodName(td.Name, md.Name, md.IsSetter)
			if _, exists := c.funcs[plainName]; !exists {
				c.funcs[plainName] = fn
			}
		}
	}
}

// defineModuleTypeMethods generates method bodies for module types.
func (c *Compiler) defineModuleTypeMethods(file *ast.File, moduleName string) {
	for _, decl := range file.Decls {
		td, ok := decl.(*ast.TypeDecl)
		if !ok {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}
		named := c.lookupNamedType(td.Name)
		if named == nil || len(named.TypeParams()) > 0 {
			continue
		}

		for _, md := range td.Methods {
			if md.Body == nil {
				continue
			}
			if len(md.TypeParams) > 0 {
				continue // generic method — handled by mono method instances
			}
			m := c.lookupAnyMethod(named, md.Name, md.IsGetter, md.IsSetter)
			if m == nil || m.Sig() == nil {
				continue
			}

			mangledName := mangleModuleMethodName(moduleName, td.Name, md.Name, md.IsSetter)
			fn, ok := c.funcs[mangledName]
			if !ok {
				continue
			}

			c.currentNamed = named

			// Route generator methods to the generator codegen path
			if elemType := c.info.GeneratorFuncs[md]; elemType != nil {
				c.defineGeneratorMethod(md, m, fn, elemType, named)
				c.currentNamed = nil
				continue
			}

			c.fn = fn
			c.block = fn.NewBlock(".entry")
			c.entryBlock = c.block
			c.locals = make(map[string]*ir.InstAlloca)
			c.localNameCount = make(map[string]int)
			c.dropFlags = make(map[string]*ir.InstAlloca)
			c.dropBindings = make(map[string]scopeBinding)
			c.scopeBindings = nil
			c.canError = m.Sig().CanError()
			c.currentRetType = m.Sig().Result()
			c.blockCounter = 0

			// Bind 'this' and parameters
			c.mutRefPtrs = nil
			c.mutRefTypes = nil
			paramIdx := 0
			if m.Sig().Recv() != nil {
				receiverType := irtypes.Type(irtypes.I8Ptr)
				if isPrimitiveScalar(named) {
					receiverType = llvmNamedType(named)
				}
				alloca := c.entryBlock.NewAlloca(receiverType)
				c.entryBlock.NewStore(fn.Params[0], alloca)
				c.locals["this"] = alloca
				paramIdx = 1
			}
			for i, p := range m.Sig().Params() {
				if _, isMutRef := p.Type().(*types.MutRef); isMutRef {
					// MutRef param: the LLVM param is a pointer to the caller's storage (B0149)
					innerType := c.resolveType(p.Type())
					if c.mutRefPtrs == nil {
						c.mutRefPtrs = make(map[string]value.Value)
						c.mutRefTypes = make(map[string]irtypes.Type)
					}
					c.mutRefPtrs[p.Name()] = fn.Params[paramIdx+i]
					c.mutRefTypes[p.Name()] = innerType
				} else {
					alloca := c.entryBlock.NewAlloca(fn.Params[paramIdx+i].Typ)
					c.entryBlock.NewStore(fn.Params[paramIdx+i], alloca)
					c.locals[p.Name()] = alloca
				}
			}

			c.genBlock(md.Body)

			if c.block != nil && c.block.Term == nil {
				if m.Sig().CanError() {
					c.block.NewRet(c.zeroValue(fn.Sig.RetType))
				} else if fn.Sig.RetType == irtypes.Void {
					c.block.NewRet(nil)
				} else {
					c.block.NewRet(c.zeroValue(fn.Sig.RetType))
				}
			}
			c.currentNamed = nil
		}
	}
}

// computeUserTypeLayouts computes layouts for all user-declared types in the file.
// Generic types (with TypeParams) are skipped — they're handled by computeMonoLayouts.
// Uses topological ordering to ensure parent layouts are computed before children.
func (c *Compiler) computeUserTypeLayouts(file *ast.File) {
	// Collect all user type decls that need layouts
	pending := make(map[string]*types.Named)
	var names []string
	for _, decl := range file.Decls {
		td, ok := decl.(*ast.TypeDecl)
		if !ok {
			continue
		}
		if c.info.FilteredDecls[decl] {
			continue // excluded by `target(cond) annotation for this build target
		}
		named := c.lookupNamedType(td.Name)
		if named == nil {
			continue
		}
		if _, exists := c.layouts[named]; exists {
			continue // skip built-in types with pre-computed layouts
		}
		if len(named.TypeParams()) > 0 {
			continue // generic — handled by monomorphization
		}
		if isNativeTypeDecl(td) {
			continue // native types have special codegen layout handling
		}
		pending[td.Name] = named
		names = append(names, td.Name)
	}

	// Compute layouts with dependency resolution (parents before children)
	computed := make(map[string]bool)
	var compute func(name string)
	compute = func(name string) {
		if computed[name] {
			return
		}
		named := pending[name]
		if named == nil {
			return
		}
		// Ensure parent layouts are computed first
		for _, pr := range named.Parents() {
			pName := pr.Named.Obj().Name()
			if _, ok := pending[pName]; ok {
				compute(pName)
			}
		}
		if named.IsValueType() {
			c.layouts[named] = computeValueTypeLayout(c.module, named, c.layouts, c.ptrSize(), c.enumLayouts)
		} else {
			c.layouts[named] = computeUserTypeLayout(c.module, named, c.layouts, c.ptrSize(), c.enumLayouts)
		}
		computed[name] = true
	}
	for _, name := range names {
		compute(name)
	}
}

