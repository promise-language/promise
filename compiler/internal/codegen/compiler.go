	isEmpty := entry.NewICmp(enum.IPredEQ, vecLen, zero64)

	emptyBlk := fn.NewBlock("empty")
	doPopBlk := fn.NewBlock("do_pop")
	entry.NewCondBr(isEmpty, emptyBlk, doPopBlk)

